class ArithmeticOperator {
  static void arithmeticOperator() {
    int a = 2;
    int b = 3;

    var c = a + b;
    print("sum of a + b = $c");

    var d = a - b;
    print("subtract of a - b = $d");

    var e = -d;
    print("Negation -(a-b) = $e");

    var f = a * b;
    print("Product of a*b = $f");

    var g = a / b;
    print("Division of a / b = $g");

    var h = b % a;
    print("modules of a % b = $h");
  }
}
